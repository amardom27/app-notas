    private void addNoteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNoteBtnActionPerformed
        // TODO add your handling code here:
        String title = titleField.getText();
        String desc = descField.getText();
        
        if (!title.isBlank() && !desc.isBlank()) {
            Usuarios usuario = UsuariosService.obtenerUsuarioPorId(this.idUsuario);
            System.out.println(usuario.toString());
            Notas nuevaNota = new Notas(title, desc, usuario);
            NotasService.agregarNota(nuevaNota);
            dispose();
        }
    }//GEN-LAST:event_addNoteBtnActionPerformed

